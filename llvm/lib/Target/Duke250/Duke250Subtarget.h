#ifndef LLVM_LIB_TARGET_Duke250_Duke250SUBTARGET_H
#define LLVM_LIB_TARGET_Duke250_Duke250SUBTARGET_H

#include "Duke250FrameLowering.h"
#include "Duke250ISelLowering.h"
#include "Duke250InstrInfo.h"
#include "llvm/CodeGen/SelectionDAGTargetInfo.h"
#include "llvm/CodeGen/TargetSubtargetInfo.h"
#include "llvm/IR/DataLayout.h"
#include "llvm/MC/MCInstrItineraries.h"

#define GET_SUBTARGETINFO_HEADER
#include "Duke250GenSubtargetInfo.inc"

namespace llvm {
class Duke250Subtarget : public Duke250GenSubtargetInfo {
protected:
  SelectionDAGTargetInfo TSInfo;
  Duke250InstrInfo InstrInfo;
  Duke250FrameLowering FrameLowering;
  Duke250TargetLowering TLInfo;
  Duke250RegisterInfo RegInfo;

public:
  /// This constructor initializes the data members to match that
  /// of the specified triple.
  Duke250Subtarget(const Triple &TT, StringRef CPU, StringRef FS,
                const TargetMachine &TM);

  /// ParseSubtargetFeatures - Parses features string setting specified
  /// subtarget options.  Definition of function is auto generated by tblgen.
  void ParseSubtargetFeatures(StringRef CPU, StringRef FS);

  Duke250Subtarget &initializeSubtargetDependencies(const Triple &TT,
                                                  StringRef CPU, StringRef FS,
                                                  const TargetMachine &TM);

  const SelectionDAGTargetInfo *getSelectionDAGInfo() const override {
    return &TSInfo;
  }

  const Duke250InstrInfo *getInstrInfo() const override {
    return &InstrInfo;
  }

  const TargetFrameLowering *getFrameLowering() const override {
    return &FrameLowering;
  }

  const Duke250RegisterInfo *getRegisterInfo() const override {
    return &RegInfo;
  }

  const Duke250TargetLowering *getTargetLowering() const override {
    return &TLInfo;
  }

  /// getMaxInlineSizeThreshold - Returns the maximum memset / memcpy size
  /// that still makes it profitable to inline the call.
  unsigned getMaxInlineSizeThreshold() const {
    return 64;
  }
};
}

#endif // end LLVM_LIB_TARGET_Duke250_Duke250SUBTARGET_H
